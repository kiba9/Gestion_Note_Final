    public void listClasse() throws SQLException {

        if (Connexion.peutLire(Classe.class)) {

            filtrer(true);

            table.setItems(listeClasse);
            activationDesactivationDetails(gridPane, false);
            codecolumn.setCellValueFactory(cellData -> new SimpleLongProperty(cellData.getValue().getCode()).asObject());
            libellecolumn.setCellValueFactory(cellData -> new SimpleStringProperty(cellData.getValue().getLibelle()));
            niveaucolumn.setCellValueFactory(cellData -> new SimpleObjectProperty<>(cellData.getValue().getNiveau()));
            specialitecolumn.setCellValueFactory(cellData -> new SimpleObjectProperty<>(cellData.getValue().getSpecialite()));

            ResultSetMetaData resultSetMetaData = new Isj().renvoyerChamp(Classe.class);
            for (int i = 1; i <= resultSetMetaData.getColumnCount(); i++) {
                try {
                    listAttributs.add(resultSetMetaData.getColumnName(i));
                } catch (SQLException e) {
                    e.printStackTrace();
                }
            }
            attributs.setItems(listAttributs);
        }
    }

    /**
     * Fonction permettant d'afficher les détails d'une classe
     *
     * @param classe variable de type ClasseController
     */

    Classe classeSelectionne = null;
    ClasseFacade classeFacade = new ClasseFacade();

    public void afficheDetail(Classe classe) {
        if (Connexion.peutLire(Classe.class)) {
            //Desactivation de tous les TextFields du panneau des détails
            activationDesactivationDetails(gridPane, false);

            if (classe != null) {
                classeSelectionne = classe;
                code.setText(String.valueOf(classe.getCode()));
                description.setText(classe.getDescription());
                libelle.setText(classe.getLibelle());
                niveau.setValue(classe.getNiveau());
                specialite.setValue(classe.getSpecialite());
            }
        }
    }

    /**
     * Fonction permettant de vider les zones de détails d'une classe pour en créer une autre
     */
    @FXML
    public void handleNouveau() {

        if (Connexion.peutEcrire(Classe.class)) {
            classeSelectionne = null;
            //Raactivation de tous TextField du panneau des détails
            activationDesactivationDetails(gridPane, true);
            code.setText("");
            description.setText("");
            libelle.setText("");
            niveau.setValue(null);
            specialite.setValue(null);

        }
    }

    /**
     * Fonction permettant d'éditer les informations d'une classe
     */
    @FXML
    public void handleModifier() {

        if (classeSelectionne == null) {
            Alert alert = new Alert(Alert.AlertType.ERROR);
            alert.initOwner(Appli.getPrimaryStage);
            alert.setTitle("ISJ");
            alert.setHeaderText("Aucune donnée à modifier");
            alert.setContentText("Veuillez sélectionner une ligne dans le tableau.");
            alert.show();
        } else if (Connexion.peutModifier(Classe.class)) {
            activationDesactivationDetails(gridPane, true);
        }
    }

    /**
     * Fonction permettant d'enregistrer une classe dans la base de données
     *
     */
    @FXML
    public void handleEnregistrer() {

        if (Connexion.peutLire(Classe.class) || Connexion.peutModifier(Classe.class)) {
            try {
                String descriptionClasse, libelleClasse;
                descriptionClasse = description.getText();
                libelleClasse = libelle.getText();
                Niveau niveauclasse = niveau.getSelectionModel().getSelectedItem();
                Specialite specialiteclasse = specialite.getSelectionModel().getSelectedItem();

                String resultat;
                if (classeSelectionne == null)
                    resultat = classeFacade.enregistrer(libelleClasse, descriptionClasse, niveauclasse,specialiteclasse);
                else
                    resultat = classeFacade.modifier(classeSelectionne, libelleClasse, descriptionClasse, niveauclasse,specialiteclasse);

                Alert alert = new Alert(Alert.AlertType.INFORMATION);
                alert.initOwner(Appli.getPrimaryStage);
                alert.setTitle("ISJ");
                alert.setHeaderText("Resultat de l'opération");
                alert.setContentText(resultat.toUpperCase() + " !");
                alert.show();

            } catch (Exception e) {
                e.printStackTrace();
                Alert alert = new Alert(Alert.AlertType.ERROR);
                alert.initOwner(Appli.getPrimaryStage);
                alert.setTitle("ISJ");
                alert.setHeaderText("Erreur lors l'opération");
                alert.setContentText(e.getMessage() + " !");
                alert.show();
            }
        }
        handleRaffraichir();
    }

    /**
     * Fonction permettant de supprimer une classe de la base de données
     */
    @FXML
    public void handleSupprimer() {

        if (Connexion.peutSupprimer(Classe.class)) {
            try {

                if (classeSelectionne != null) {
                    Alert alert = new Alert(Alert.AlertType.CONFIRMATION);
                    alert.setTitle("ISJ");
                    alert.setHeaderText("Confirmation de Suppression");
                    alert.setContentText("Voulez-vous vraiment supprimer la donnée ?");

                    Optional<ButtonType> result = alert.showAndWait();
                    if (result.get() == ButtonType.OK) {
                        String suppression = classeFacade.remove(classeSelectionne);
                        if (suppression != null && suppression.equalsIgnoreCase("succes"))
                            table.getItems().remove(classeSelectionne);
                        else {
                            alert = new Alert(Alert.AlertType.ERROR);
                            alert.initOwner(Appli.getPrimaryStage);
                            alert.setTitle("ISJ");
                            alert.setHeaderText("La donnée ne peut être supprimée.");
                            alert.setContentText("Il est possible qu'une contrainte d'intégrité empêche la suppression de la donnée.");
                            alert.show();
                        }
                    } else {
                        alert.close();
                    }

                } else {
                    Alert alert = new Alert(Alert.AlertType.WARNING);
                    alert.initOwner(Appli.getPrimaryStage);
                    alert.setTitle("ISJ");
                    alert.setHeaderText("Aucune donnée sélectionnée.");
                    alert.setContentText("Veuillez sélectionner une ligne dans le tableau.");
                    alert.show();
                }

            } catch (Exception e) {
                e.getMessage();
            }
        }
    }

